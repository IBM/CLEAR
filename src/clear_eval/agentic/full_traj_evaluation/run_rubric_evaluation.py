# 7. Single trajectory evaluation
# ---------------------------------------------------------------------------

async def evaluate_single(
    entry: dict,
    session: aiohttp.ClientSession,
    rubrics_dir: Path,
    results_dir: Path,
    judge_model_id: str,
    judge_short_name: str,
    judge_key: str,
    provider: str = "rits",
    context_tokens: int = 128_000,
    overwrite: bool = False,
) -> dict | None:
    """Evaluate a single trajectory against its rubrics and save results."""
    dataset = entry["dataset"]
    model_name = entry["model_name"]
    file_path = entry["file_path"]
    traj_name = entry["traj_name"]

    output_dir = results_dir / dataset / model_name
    output_file = output_dir / f"{traj_name}_rubric_eval.json"

    if output_file.exists() and not overwrite:
        logger.info("Skipping (exists): %s/%s/%s", dataset, model_name, traj_name)
        return None

    # Load rubrics
    rubric_file = rubrics_dir / dataset / model_name / f"{traj_name}_rubrics.json"
    if not rubric_file.exists():
        logger.warning(
            "No rubrics for %s/%s/%s — run generate_task_rubrics.py first",
            dataset, model_name, traj_name,
        )
        return None

    try:
        with open(rubric_file, "r", encoding="utf-8") as f:
            rubric_data = json.load(f)
    except Exception as e:
        logger.error("Failed to load rubrics %s: %s", rubric_file, e)
        return None

    rubrics = rubric_data.get("rubrics", [])
    task_objective = rubric_data.get("task_objective", "")

    if not rubrics:
        logger.warning(
            "Empty rubrics for %s/%s/%s — skipping", dataset, model_name, traj_name,
        )
        return None

    # Load trajectory
    try:
        with open(file_path, "r", encoding="utf-8") as f:
            traj_data = json.load(f)
    except Exception as e:
        logger.error("Failed to load %s: %s", file_path, e)
        return None

    dataset_obj = get_dataset_obj(dataset_name=dataset, data_dir="")
    trajectory_text = dataset_obj.format_trajectory(traj_data)
    max_traj_chars = get_max_trajectory_chars()

    prompt = build_rubric_eval_prompt(
        task_objective, rubrics, trajectory_text, max_len=max_traj_chars,
    )

    #logger.info("Evaluating: %s/%s/%s (%d rubrics)", dataset, model_name, traj_name, len(rubrics))
    start_time = time.time()

    response_text = await call_llm_async(
        prompt,
        system_message=SYSTEM_MESSAGE_RUBRIC_EVAL,
        provider=provider,
        model_id=judge_model_id,
        session=session,
    )

    elapsed = time.time() - start_time

    if not response_text:
        logger.error("No response for %s/%s/%s", dataset, model_name, traj_name)
        return None

    evaluation = parse_evaluation_response(response_text)

    rubric_results = {}
    summary = None
    score = None
    fulfilled_count = 0
    total_count = len(rubrics)

    if evaluation:
        rubric_results = evaluation.get("rubric_results", {})
        summary = evaluation.get("summary")
        raw_score = evaluation.get("score")
        try:
            score = float(raw_score)
        except (ValueError, TypeError):
            pass

        for rid, rdata in rubric_results.items():
            if isinstance(rdata, dict):
                raw_f = rdata.get("fulfilled")
                try:
                    if int(raw_f) == 1:
                        fulfilled_count += 1
                except (ValueError, TypeError):
                    pass

        if score is None and total_count > 0:
            score = round(fulfilled_count / total_count, 2)

    result = {
        "trajectory_name": traj_name,
        "dataset": dataset,
        "model_name": model_name,
        "source_file": str(file_path),
        "rubrics_file": str(rubric_file),
        "judge_model": judge_model_id,
        "evaluation_timestamp": datetime.now().isoformat(),
        "evaluation_time_seconds": round(elapsed, 2),
        "task_objective": task_objective,
        "rubrics": rubrics,
        "num_rubrics": total_count,
        "rubric_results": rubric_results,
        "fulfilled_count": fulfilled_count,
        "summary": summary,
        "score": score,
        "raw_response": response_text,
        "parsed_evaluation": evaluation,
    }

    output_dir.mkdir(parents=True, exist_ok=True)
    with open(output_file, "w", encoding="utf-8") as f:
        json.dump(result, f, indent=2, ensure_ascii=False)

    # logger.info(
    #     "Saved: %s (fulfilled=%d/%d, score=%.2f, time=%.1fs)",
    #     output_file.relative_to(results_dir),
    #     fulfilled_count,
    #     total_count,
    #     score if score is not None else 0,
    #     elapsed,
    # )

    return result



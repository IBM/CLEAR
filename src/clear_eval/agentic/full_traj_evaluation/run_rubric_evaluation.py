def parse_args():
    parser = create_base_parser("Rubric Evaluation")
    parser.add_argument(
        "--rubrics-model-id",
        type=str,
        default=None,
        help=(
            "Model ID used to generate rubrics (defaults to --model-id). "
            "Use this if rubrics were generated by a different model."
        ),
    )
    return parser.parse_args()


async def async_main(args):
    """Async entry point."""
    judge_model_id = args.model_id
    rubrics_model_id = args.rubrics_model_id or args.model_id
    rubrics_dir = get_rubrics_dir(rubrics_model_id)
    results_dir = get_results_dir("rubric_eval_results", args.model_id)
    context_tokens = args.context_tokens

    if args.summary_only:
        if results_dir.exists():
            summary = generate_summary_report(results_dir)
            print_summary(summary, judge_model_id)
            summary_file = results_dir / "summary.json"
            with open(summary_file, "w") as f:
                json.dump(summary, f, indent=2)
            print(f"\nSummary saved to: {summary_file}")
        else:
            print(f"No results found at {results_dir}. Run evaluation first.")
        return

    if not rubrics_dir.exists():
        print(f"Rubrics directory not found: {rubrics_dir}")
        print("Run generate_task_rubrics.py first to generate rubrics.")
        sys.exit(1)

    entries = discover_trajectories(
        TRAJ_DATA_DIR,
        filter_dataset=args.dataset,
        filter_model=args.model,
    )

    if not entries:
        print("No trajectory files found. Check paths and filters.")
        return

    if args.max_files:
        grouped = defaultdict(list)
        for e in entries:
            key = (e["dataset"], e["model_name"])
            grouped[key].append(e)
        entries = []
        for key, group in grouped.items():
            entries.extend(group[: args.max_files])

    counts = Counter((e["dataset"], e["model_name"]) for e in entries)

    max_traj_chars = get_max_trajectory_chars(context_tokens)

    print("=" * 70)
    print("Rubric-Based Evaluation Plan")
    print("=" * 70)
    print(f"Provider:     {args.provider}")
    print(f"Judge Model:  {judge_model_id}")
    print(f"Rubrics from: {rubrics_dir.name}/")
    print(f"Context Win:  {context_tokens:,} tokens → "
          f"max trajectory {max_traj_chars:,} chars")
    print(f"Data Dir:     {TRAJ_DATA_DIR}")
    print(f"Results Dir:  {results_dir}")
    print(f"Overwrite:    {args.overwrite}")
    print(f"Concurrency:  {args.concurrency}")
    print(f"Total files:  {len(entries)}")
    print()
    for (ds, model), cnt in sorted(counts.items()):
        print(f"  {ds}/{model}: {cnt} files")
    print("=" * 70)

    start = time.time()
    results = await evaluate_batch(
        entries,
        rubrics_dir=rubrics_dir,
        results_dir=results_dir,
        judge_model_id=judge_model_id,
        judge_short_name="",  # Not needed anymore
        judge_key=args.model_id,
        provider=args.provider,
        context_tokens=context_tokens,
        overwrite=args.overwrite,
        concurrency=args.concurrency,
    )
    elapsed = time.time() - start

    print(f"\nEvaluation complete: {len(results)} trajectories in {elapsed:.1f}s")

    if results_dir.exists():
        summary = generate_summary_report(results_dir)
        print_summary(summary, judge_model_id)

        summary_file = results_dir / "summary.json"
        with open(summary_file, "w") as f:
            json.dump(summary, f, indent=2)
        print(f"\nSummary saved to: {summary_file}")


def main():
    args = parse_args()

    if not args.summary_only:
        if not RITS_API_KEY:
            print("ERROR: RITS_API_KEY not set.")
            print("  Export it:    export RITS_API_KEY='your-key'")
            print("  Or .env file: RITS_API_KEY=your-key")
            sys.exit(1)

    asyncio.run(async_main(args))


if __name__ == "__main__":
    main()

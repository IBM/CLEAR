def generate_summary_report(results_dir: Path) -> dict:
    """Generate a summary from rubric evaluation results."""
    summary = {}

    for dataset_dir in sorted(results_dir.iterdir()):
        if not dataset_dir.is_dir() or dataset_dir.name.startswith("."):
            continue
        dataset = dataset_dir.name
        summary[dataset] = {}

        for model_dir in sorted(dataset_dir.iterdir()):
            if not model_dir.is_dir() or model_dir.name.startswith("."):
                continue
            model_name = model_dir.name

            eval_files = list(model_dir.glob("*_rubric_eval.json"))
            if not eval_files:
                continue

            scores: list[float] = []
            total_rubrics = 0
            total_fulfilled = 0
            count = 0

            for ef in eval_files:
                try:
                    with open(ef, "r") as f:
                        data = json.load(f)
                    sc = data.get("score")
                    if isinstance(sc, (int, float)):
                        scores.append(float(sc))
                    nr = data.get("num_rubrics", 0)
                    fc = data.get("fulfilled_count", 0)
                    total_rubrics += nr
                    total_fulfilled += fc
                    count += 1
                except Exception:
                    continue

            avg_score = None
            if scores:
                avg_score = round(sum(scores) / len(scores), 3)

            fulfillment_rate = None
            if total_rubrics > 0:
                fulfillment_rate = round(
                    total_fulfilled / total_rubrics, 3,
                )

            summary[dataset][model_name] = {
                "total_evaluations": count,
                "average_score": avg_score,
                "min_score": round(min(scores), 3) if scores else None,
                "max_score": round(max(scores), 3) if scores else None,
                "total_rubrics": total_rubrics,
                "total_fulfilled": total_fulfilled,
                "overall_fulfillment_rate": fulfillment_rate,
            }

    return summary


def print_summary(summary: dict, judge_model_id: str):
    """Pretty-print the summary report."""
    print("\n" + "=" * 80)
    print("RUBRIC-BASED EVALUATION SUMMARY")
    print(f"Judge Model: {judge_model_id}")
    print("=" * 80)

    for dataset, models in summary.items():
        print(f"\n{'#' * 70}")
        print(f"# Dataset: {dataset}")
        print(f"{'#' * 70}")

        for model_name, stats in models.items():
            avg = stats.get("average_score")
            avg_str = f"{avg:.3f}" if avg is not None else "N/A"
            min_s = stats.get("min_score")
            max_s = stats.get("max_score")
            range_str = (
                f"{min_s:.3f} – {max_s:.3f}"
                if min_s is not None and max_s is not None
                else "N/A"
            )
            fr = stats.get("overall_fulfillment_rate")
            fr_str = f"{fr:.1%}" if fr is not None else "N/A"

            print(f"\n  Model: {model_name}")
            print(f"  Evaluations:           {stats['total_evaluations']}")
            print(f"  Average Score:         {avg_str}")
            print(f"  Score Range:           {range_str}")
            print(f"  Total Rubrics:         {stats['total_rubrics']}")
            print(f"  Total Fulfilled:       {stats['total_fulfilled']}")
            print(f"  Overall Fulfillment:   {fr_str}")

    print("\n" + "=" * 80)



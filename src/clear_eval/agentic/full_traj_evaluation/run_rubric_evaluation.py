"""
Rubric-Based Trajectory Evaluation via RITS  (Step 2)
=======================================================

Evaluates agent trajectories against task-specific rubrics generated by
``generate_task_rubrics.py`` (Step 1).

Workflow:
    1. Load pre-generated rubrics for each trajectory from:
         rubrics_{judge}/{dataset}/{model}/{traj}_rubrics.json
    2. Format the full execution trace.
    3. Prompt a judge model with the trace + rubrics and ask it to assess
       whether each rubric was fulfilled.
    4. The judge produces, for each rubric:
         - ``fulfilled``: 1 (yes) or 0 (no)
         - ``reasoning``: 1-3 sentences explaining the assessment.
       Plus an overall ``summary`` and ``score`` (fraction of rubrics met).
    5. Save results to:
         rubric_eval_results_{judge}/{dataset}/{model}/{traj}_rubric_eval.json

Supported judge models (--judge flag):
    oss20b   - openai/gpt-oss-20b   (default, fast for debugging)
    oss120b  - openai/gpt-oss-120b
    deepseek - deepseek-ai/DeepSeek-V3.2

Setup:
    1. Create a ``.env`` file (or export) with:
         RITS_API_KEY=<your-rits-api-key>
    2. Install dependencies:
         pip install aiohttp python-dotenv tqdm
    3. First, generate rubrics:
         python generate_task_rubrics.py --judge oss120b
    4. Then, run this evaluation:
         python run_rubric_evaluation.py --judge oss120b

Usage examples:
    python run_rubric_evaluation.py
    python run_rubric_evaluation.py --judge oss120b
    python run_rubric_evaluation.py --dataset CUGA --model full --max-files 5
    python run_rubric_evaluation.py --summary-only
"""


"""
Shared Argument Parsing
=======================

Common argument parsing utilities for evaluation scripts.
"""

import argparse
from typing import Optional


def create_base_parser(description: str) -> argparse.ArgumentParser:
    """
    Create base argument parser with common arguments.
    
    Args:
        description: Script description
        
    Returns:
        ArgumentParser with common arguments added
    """
    parser = argparse.ArgumentParser(description=description)
    
    # Provider and model arguments
    parser.add_argument(
        "--provider",
        type=str,
        default="watsonx",
        choices=["rits", "watsonx", "openai"],
        help="LLM provider to use (default: watsonx)",
    )
    parser.add_argument(
        "--model-id",
        type=str,
        default="openai/gpt-oss-120b",
        help="Model ID for the judge (default: openai/gpt-oss-120b)",
    )
    parser.add_argument(
        "--context-tokens",
        type=int,
        default=128_000,
        help="Model context window size in tokens (default: 128000)",
    )
    
    # Dataset and model filtering
    parser.add_argument(
        "--dataset",
        type=str,
        default="HAL",
        choices=["CUGA","gaia","swebench", "HAL","TRAIL"], #["CUGA", "FinOps", "WXO"],
        help="Filter by dataset name (default: CUGA)",
    )
    parser.add_argument(
        "--model",
        type=str,
        default="orig_results",
        help="Filter by model name (e.g., gpt4o, full, granit)",
    )
    
    # Execution control
    parser.add_argument(
        "--overwrite",
        action="store_true",
        help="Re-evaluate even if results already exist",
    )
    parser.add_argument(
        "--max-files",
        type=int,
        default=None,
        help="Max files to evaluate per dataset/model combo",
    )
    parser.add_argument(
        "--concurrency",
        type=int,
        default=10,
        help="Number of concurrent API requests (default: 10)",
    )
    parser.add_argument(
        "--summary-only",
        action="store_true",
        help="Only generate summary from existing results (no evaluation)",
    )
    
    return parser


def add_rubric_specific_args(parser: argparse.ArgumentParser) -> argparse.ArgumentParser:
    """
    Add rubric-evaluation specific arguments.
    
    Args:
        parser: Base argument parser
        
    Returns:
        Parser with rubric-specific arguments added
    """
    parser.add_argument(
        "--rubrics-model-id",
        type=str,
        default=None,
        help=(
            "Model ID used to generate rubrics (defaults to --model-id). "
            "Use this if rubrics were generated by a different model."
        ),
    )
    return parser

